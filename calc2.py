# calculadora simples otimizada


def soma(n1, n2):
    return n1 + n2

def sub(n1, n2):
    return n1 - n2

def mult(n1, n2):
    return n1 * n2

def div(n1, n2):
    return n1/n2

def porc(n1, n2):
    return  n1/100 * n2



